package operators;

public class AithmeticOperators {
    public static void main(String[] args) {

        // =
        int age = 18;
                     // age = 20
                    // age = 18 + 2
         age += 2; // age = age + 2;
        System.out.println(age);

        int num = 5;
        num = 89;
        System.out.println(num); //89
        num = 100;
        System.out.println(num); //100
        num -=35;
        System.out.println(num);

        //*=
        num *= 3;
        System.out.println(num);

        // /=
        num /= 5;
        System.out.println(num);

        // %=
        num %= 7;
        System.out.println(num);

    }
}
